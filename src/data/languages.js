export const LANGUAGES = TRANSLATED_LANGUAGES.map((translatedName, index) => {
  const englishName = ENGLISH_LANGUAGES[index] || translatedName;

  const baseIdSource = englishName || translatedName;
  const baseId = baseIdSource
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');

  const id = baseId || `lang-${index}`;

  return {
    id,
    translatedName,
    englishName,
  };
});

export const TRANSLATED_LANGUAGE_NAMES = LANGUAGES.map(
  (language) => language.translatedName
);

export const ENGLISH_LANGUAGE_NAMES = LANGUAGES.map(
  (language) => language.englishName
);

